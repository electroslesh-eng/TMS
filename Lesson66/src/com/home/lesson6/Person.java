package com.home.lesson6;

public class Person {
    private String name;
    private int age;
    private boolean Male;
    private String sity;
    private String country;

    public Person(String name, int age, boolean male, String sity, String country) {
        this.name = name;
        this.age = age;
        Male = male;
        this.sity = sity;
        this.country = country;
    }
}
